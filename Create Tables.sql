CREATE INDEX IX_USER_MESSAGE_FOLDERS_FOLDER_ID ON USER_MESSAGE_FOLDERS (FOLDER_ID);
CREATE INDEX IX_USER_MESSAGE_FOLDERS_MESSAGE_ID ON USER_MESSAGE_FOLDERS (USER_MESSAGE_ID);

