CREATE TABLE [USER_MESSAGE_FOLDERS] (
    [ID] INT IDENTITY(1,1) NOT NULL,
    [USER_MESSAGE_ID] INT NOT NULL,
    [FOLDER_ID] INT NOT NULL,
    CONSTRAINT PK_USER_MESSAGE_FOLDERS PRIMARY KEY (ID),
	CONSTRAINT FK_USER_MESSAGE_FOLDERS_USER_MESSAGE FOREIGN KEY (USER_MESSAGE_ID) REFERENCES USER_MESSAGES(ID),
	CONSTRAINT FK_USER_MESSAGE_FOLDERS_FOLDERS FOREIGN KEY (FOLDER_ID)REFERENCES FOLDERS(ID)
